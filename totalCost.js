function totalCost(calls) {
	var total = 0;
	var data = ['',0];
	var flag = false;
	for (let call of calls) {
		call = call.split(' ');
		var call_duration = Math.ceil(parseInt(call[2])/60.0);
		if (call[0] != data[0]) {
			data[0] = call[0];
			data[1] = call_duration;
			flag = false;
		} else {
			data[1] += call_duration;
		}
		if (data[1] >= 100 && !flag) {
			flag = true;
			var m = data[1] - call_duration;
			var n = 100 - m;
			var o = call_duration - n
			total += n + (o * 2);
			//console.log(m,n,o)
		} else if (flag) {
			total += call_duration * 2;
		} else {
			total += call_duration;
		}
	}
	console.log(total);
}

